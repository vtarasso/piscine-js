function more(argument) {
   let all = argument + 1;
   return all
}

function less(argument) {
    let all = argument - 1;
    return all
}

function add(argument1, argument2) {
    let all = argument1 + argument2;
    return all
}

function sub(argument1, argument2) {
    let all = argument1 - argument2;
    return all
}